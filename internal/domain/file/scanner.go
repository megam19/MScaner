package file

import "context"

// Scanner — доменный интерфейс источника файлов.
//
// Абстракция над «местом, где лежат файлы». Конкретная реализация (папка
// на диске, сетевая шара, S3-бакет и т. д.) живёт в infrastructure.
// Домен лишь хочет получить срез File на данный момент времени.
type Scanner interface {
	// Scan возвращает список файлов, находящихся сейчас по указанному пути.
	// path — абстракция источника: для файловой системы это путь к папке,
	// для другой реализации может быть URL, идентификатор и т. п.
	Scan(ctx context.Context, path string) ([]File, error)
}
