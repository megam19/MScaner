// Package file — доменная модель файла (слой Domain в DDD).
//
// Здесь живёт «язык предметной области»: что такое File, когда он считается
// новым, когда удалённым. Пакет НЕ знает про SQLite, HTTP, файловую систему,
// JSON и т. п. Это самый нижний слой — он не зависит ни от чего, все
// остальные слои зависят от него.
//
// Правило: если вы собираетесь добавить сюда импорт вроде "database/sql",
// "net/http" или "os" — это ошибка. Такие вещи должны быть в infrastructure.
package file

import "time"

// File — доменная сущность: запись о файле, найденном при сканировании папки.
//
// Поле DeletedAt — указатель, потому что nil означает «не удалён» (живой файл),
// а конкретное время — «файл пропал из папки, запись помечена удалённой».
// Это «мягкое удаление» (soft-delete): физически запись в БД остаётся,
// чтобы показывать историю в UI и отличать «никогда не видели» от «было и пропало».
type File struct {
	Name      string     // имя файла (без пути)
	Size      int64      // размер в килобайтах
	CreatedAt time.Time  // когда запись впервые появилась в БД
	UpdatedAt time.Time  // когда запись последний раз обновлялась (напр., изменился размер)
	DeletedAt *time.Time // nil, если файл жив; иначе — момент пометки удалённым
}

// IsDeleted — файл помечен как удалённый (пропал из папки).
func (f File) IsDeleted() bool {
	return f.DeletedAt != nil
}

// IsNew — файл считается «только что добавленным» в пределах окна window.
// Используется UI-слоем для подсветки недавних файлов.
// Удалённые файлы «новыми» не считаются, даже если CreatedAt свежий.
func (f File) IsNew(now time.Time, window time.Duration) bool {
	return f.DeletedAt == nil && now.Sub(f.CreatedAt) <= window
}

// New — удобный конструктор новой доменной сущности.
// Сейчас используется редко (чаще File собирается из данных БД), оставлен
// как «документация» того, каким File попадает в систему впервые.
func New(name string, size int64) File {
	now := time.Now().UTC()
	return File{
		Name:      name,
		Size:      size,
		CreatedAt: now,
		UpdatedAt: now,
	}
}
